	SUBROUTINE JRC(T,JK)
	IMPLICIT NONE
	REAL(KIND = DBL), INTENT(IN)::T
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::JK
	INTEGER:: J
	COMPLEX*16 :: ADAT,BDAT,CDAT
	J = NINT(T/0.0025D0)
	ADAT = ADATA(J + 1)
	BDAT = BDATA(J + 1)
	CDAT = CDATA(J + 1)
	JK= G*(ADAT*Jx + BDAT*Jy + CDAT*Jz) !making changes here
	END SUBROUTINE JRC
	
	COMPLEX*16 FUNCTION JRCMN(N,M,T)
	IMPLICIT NONE
	REAL(KIND = DBL), INTENT(IN)::T
	INTEGER, INTENT(IN)::M,N
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS)::JK,JK_EB
	COMPLEX*16, DIMENSION(DIMSYS) :: TEMP
	CALL JRC(T,JK)
	TEMP = MATMUL(JK,BASISSTATES(:,N))!making changes here
	JRCMN = DOT_PRODUCT(BASISSTATES(:,M), TEMP)
	END FUNCTION JRCMN
	
	COMPLEX*16 FUNCTION TOTALL (M,N,B,A,T) 
	IMPlICIT NONE
	INTEGER, INTENT(IN)::M,N,B,A
	REAL(KIND = DBL),INTENT(IN) :: T
	COMPLEX*16 ::TEMP1,TEMP2,TEMP3,TEMP4,SUMM1,SUMM3,SUMM
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS)::JR,JRD
	INTEGER :: K,J
	SUMM1= (0.0D0,0.0D0)
	SUMM3= (0.0D0,0.0D0)
	DO K = 1, DIMSYS
	TEMP1=KRONDELTA(M,B)*JRCMN(A,K,T)*FMN(K,N)
	SUMM1=SUMM1+TEMP1
	END DO
	DO J = 1, DIMSYS
	TEMP3=FMN(M,J)*DCONJG(JRCMN(B,J,T))*KRONDELTA(A,N)
	SUMM3=SUMM3+TEMP3
	END DO
	TEMP2=FMN(M,B)*JRCMN(A,N,T)
	TEMP4=DCONJG(JRCMN(B,M,T))*FMN(A,N)
	SUMM =  SUMM1 - TEMP2 - SUMM3 + TEMP4
	TOTALL = -0.5D0*IMU*SUMM
	END FUNCTION TOTALL
	
	SUBROUTINE BRIG(T,ENDD)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT),INTENT(OUT)::ENDD
	REAL(KIND = DBL), INTENT(IN):: T
	INTEGER :: I,J,L1,L2,L3,L4
	DO I = 1,DIMMAT
	DO J = 1,DIMMAT
	CALL INDEXCONVERTER(I,L1,L2)
	CALL INDEXCONVERTER(J,L3,L4)
	ENDD(I,J) = TOTALL(L1,L2,L3,L4,T)
	WRITE(9,*) T, ENDD(I,J)
	END DO
	END DO
	END SUBROUTINE BRIG
	
