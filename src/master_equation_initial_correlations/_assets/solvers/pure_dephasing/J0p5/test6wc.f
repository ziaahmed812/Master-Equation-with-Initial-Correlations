	INTEGER FUNCTION KRONDELTA(M,N)
	IMPLICIT NONE
	INTEGER, INTENT(IN)::N,M
	IF (N == M) THEN
		KRONDELTA = 1
	ELSE
		KRONDELTA = 0
	END IF
	END FUNCTION KRONDELTA

	SUBROUTINE MATJz(MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::MATOUT
	INTEGER::K,L
	DO K = 1,DIMSYS
	DO L = 1,DIMSYS
	MATOUT(K,L) = ((JSYS+1) - K)*KRONDELTA(K,L)
	END DO
	END DO
	END SUBROUTINE MATJz

	SUBROUTINE MATJplus(MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::MATOUT
	INTEGER::K,L
	REAL(KIND = DBL) :: M,MP
	DO K = 1,DIMSYS
	DO L = 1,DIMSYS
	MP = (JSYS + 1.0D0) - L
	M = (JSYS + 1.0D0) - K
	MATOUT(K,L) = DSQRT(JSYS*(JSYS+1.0D0) - M * (M + 1.0D0))
     &  		* KRONDELTA(NINT(2*MP),NINT(2*(M + 1)))
	END DO 	
	END DO
	END SUBROUTINE MATJplus

	SUBROUTINE MATJminus(MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::MATOUT
	INTEGER::K,L
	REAL(KIND = DBL)::M,MP
	DO K = 1,DIMSYS
	DO L = 1,DIMSYS
	MP = (JSYS + 1.0D0) - L
	M = (JSYS + 1.0D0) - K
	MATOUT(K,L) = DSQRT(JSYS*(JSYS+1.0D0) - M * (M - 1.0D0)) 
     &			* KRONDELTA(NINT(2*MP),NINT(2*(M - 1)))
	END DO 	
	END DO
	END SUBROUTINE MATJminus

	SUBROUTINE MATJx(MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::MATOUT
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS)::Jp,Jm
	CALL MATJplus(Jp)
	CALL MATJminus(Jm)
	MATOUT = (Jp + Jm)/2.0D0
	END SUBROUTINE MATJx

	SUBROUTINE MATJy(MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::MATOUT
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS)::Jp,Jm
	CALL MATJplus(Jp)
	CALL MATJminus(Jm)
	MATOUT = (Jp - Jm)/(2.0D0 * IMU)
	END SUBROUTINE


	SUBROUTINE MATID(MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::MATOUT
	INTEGER :: J
	MATOUT(:,:) = (0.0D0,0.0D0)
	DO J = 1, DIMSYS
	MATOUT(J,J) = (1.0D0,0.0D0)
	END DO
	END SUBROUTINE MATID

	SUBROUTINE INDEXCONVERTER(N,K,L)
	IMPLICIT NONE
	INTEGER, INTENT(IN)::N
	INTEGER, INTENT(OUT)::K,L
	INTEGER::X, REMAINDER
	X = N/DIMSYS
	REMAINDER = N - (X * DIMSYS)
	IF (REMAINDER == 0) THEN 
	K = X
	L = DIMSYS
	ELSE
	K = X + 1
	L = REMAINDER
	END IF
	END SUBROUTINE INDEXCONVERTER

	SUBROUTINE DENSITYMATRIX(X,RHO)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,1),INTENT(IN)::X
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::RHO
	INTEGER :: J, K, L
	DO J = 1, DIMMAT
	CALL INDEXCONVERTER(J,K,L)
	RHO(K,L) = X(J,1)
	END DO
	END SUBROUTINE DENSITYMATRIX

	SUBROUTINE EXPECTATION(X,A,ANS)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,1),INTENT(IN) :: X
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS),INTENT(IN)::A
	REAL(KIND = DBL), INTENT(OUT) :: ANS
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS)::RHO
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS) :: TEMP
	INTEGER :: J
	REAL(KIND = DBL)::SUMM
	SUMM = 0.0D0
	CALL DENSITYMATRIX(X,RHO)
	TEMP = MATMUL(A,RHO)
	DO J = 1, DIMSYS
	SUMM = SUMM + DREAL(TEMP(J,J))
	END DO
	ANS = SUMM
	END SUBROUTINE EXPECTATION

	SUBROUTINE SURVIVALPROBABILITY(X,PROB)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,1),INTENT(IN) :: X
	REAL(KIND = DBL), INTENT(OUT) :: PROB
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS)::RHO
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS) :: TEMP
	INTEGER :: J
	REAL(KIND = DBL)::SUMM
	SUMM = 0.0D0
	CALL DENSITYMATRIX(X,RHO)
	TEMP = MATMUL(INSTATE_EB,RHO)
	DO J = 1, DIMSYS
	SUMM = SUMM + DREAL(TEMP(J,J))
	END DO
	PROB = SUMM
	END SUBROUTINE SURVIVALPROBABILITY
	
		
	REAL (KIND = DBL) FUNCTION ENERGYDIFF(M,N)
	IMPLICIT NONE
	INTEGER, INTENT(IN)::N,M
	ENERGYDIFF = ENERGIES(M) - ENERGIES(N)
	END FUNCTION ENERGYDIFF
	
	
	SUBROUTINE COHEVOL(DARRAY)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMMAT,DIMMAT),INTENT(OUT)::DARRAY
	INTEGER :: I,J,L1,L2,L3,L4
	DARRAY(:,:) = (0.0D0,0.0D0)
	DO I = 1,DIMMAT
	DO J = 1,DIMMAT
	CALL INDEXCONVERTER(I,L1,L2)
	CALL INDEXCONVERTER(J,L3,L4)
	IF (L1 == L3 .AND. L2 == L4) THEN
	DARRAY(I,J) = IMU * ENERGYDIFF(L2,L1)
	END IF
	END DO
	END DO
	END SUBROUTINE COHEVOL
	 
			
	COMPLEX*16 FUNCTION FMN(M,N)
	IMPLICIT NONE
	INTEGER, INTENT(IN) :: M,N
	COMPLEX*16, DIMENSION(DIMSYS) :: TEMP
	TEMP = MATMUL(F,BASISSTATES(:,N))
	FMN = DOT_PRODUCT(BASISSTATES(:,M), TEMP)
	END FUNCTION FMN

	SUBROUTINE UROT(UMAT)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS),INTENT(OUT)::UMAT
	INTEGER::I,J
	DO I = 1, DIMSYS
	DO J = 1, DIMSYS
C	UMAT(I,J) = DOT_PRODUCT(EIGENSTATES(:,I),ID(:,J))
	UMAT(I,J) = DCONJG(EIGENSTATES(J,I))
	END DO 
	END DO
	END SUBROUTINE UROT


	SUBROUTINE INSTATETOX_EB(X)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,1),INTENT(OUT)::X
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS):: MATRIX
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS)::R
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS)::RT
	INTEGER :: K,L,J
	CALL UROT(R)
	RT = DCONJG(TRANSPOSE(R))
	MATRIX = MATMUL(R,MATMUL(INSTATE,RT))
	DO K = 1, DIMSYS
	DO L = 1, DIMSYS
	J = (K - 1)*DIMSYS + L
	X(J,1) = MATRIX(K,L)
	END DO 
	END DO
	END SUBROUTINE INSTATETOX_EB


	SUBROUTINE MATRIX_EB(MATRIX, MATOUT)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS),INTENT(IN)::MATRIX
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS),INTENT(OUT)::MATOUT
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS)::R
	COMPLEX*16, DIMENSION(DIMSYS, DIMSYS)::RT
	CALL UROT(R)
	RT = DCONJG(TRANSPOSE(R))
	MATOUT = MATMUL(R,MATMUL(MATRIX,RT))
	END SUBROUTINE MATRIX_EB

	SUBROUTINE DENSITYMATRIX_EB(X, MATOUT)
	IMPLICIT NONE
	COMPLEX*16,DIMENSION(DIMMAT, 1),INTENT(IN)::X
	COMPLEX*16,DIMENSION(DIMSYS, DIMSYS),INTENT(OUT)::MATOUT
	COMPLEX*16,DIMENSION(DIMSYS, DIMSYS)::RHO
	COMPLEX*16,DIMENSION(DIMSYS, DIMSYS)::R
	COMPLEX*16,DIMENSION(DIMSYS, DIMSYS)::RT
	CALL UROT(R)
	CALL DENSITYMATRIX(X,RHO)
	RT = DCONJG(TRANSPOSE(R))
	MATOUT = MATMUL(R,MATMUL(RHO,RT))
	END SUBROUTINE DENSITYMATRIX_EB
	 	
	COMPLEX*16 FUNCTION CORRELATOR(TAU) 		
	IMPLICIT NONE
	REAL(KIND = DBL), INTENT(IN)::TAU
	REAL(KIND = DBL) :: NU, ETA
	REAL(KIND = DBL) :: TEMP1, TEMP2
	INTEGER :: J
	J = NINT(TAU/0.0025D0)
	NU = G * NUARRAY(J + 1)
	TEMP1 = (2.0D0 * G * TAU)*OMEGAC**3.0D0
	TEMP2 = (1.0D0 + (TAU * OMEGAC)**2.0D0)**2.0D0
	ETA = TEMP1/TEMP2
	CORRELATOR = NU - IMU * ETA
	END FUNCTION CORRELATOR

	COMPLEX*16 FUNCTION WD(M,N,TAU)
	IMPLICIT NONE
	INTEGER, INTENT(IN)::M,N
	REAL(KIND = DBL), INTENT(IN)::TAU
	WD = (CDEXP(-IMU * ENERGYDIFF(M,N) * TAU)) * CORRELATOR(TAU)
	END FUNCTION WD
		
	COMPLEX*16 FUNCTION KD(M,N,B,A,TAU)
	IMPLICIT NONE
	INTEGER, INTENT(IN)::B,A,N,M
	REAL(KIND = DBL),INTENT(IN) :: TAU
	COMPLEX*16 :: T1,T2,T3
	COMPLEX*16 :: SUMM
	INTEGER :: L
	SUMM = (0.0D0,0.0D0)
	T1 = FMN(A,N)*FMN(M,B)*(WD(M,B,TAU)+DCONJG(WD(N,A,TAU)))
	DO L = 1, DIMSYS
	T2 = KRONDELTA(M,B)*FMN(L,N)*FMN(A,L)*DCONJG(WD(L,A,TAU))
	T3 = KRONDELTA(A,N)*FMN(M,L)*FMN(L,B)*WD(L,B,TAU)
	SUMM = SUMM + T2 + T3
	END DO
	KD = T1 - SUMM
	END FUNCTION KD
	
