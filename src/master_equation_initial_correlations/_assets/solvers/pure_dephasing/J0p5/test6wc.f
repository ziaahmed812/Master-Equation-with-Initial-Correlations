	SUBROUTINE KERNEL(TAU,AKER)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT),INTENT(OUT)::AKER
	REAL(KIND = DBL), INTENT(IN)::TAU
	INTEGER :: I,J,L1,L2,L3,L4
	DO I = 1,DIMMAT
	DO J = 1,DIMMAT
	CALL INDEXCONVERTER(I,L1,L2)
	CALL INDEXCONVERTER(J,L3,L4)
	AKER(I,J) = KD(L1,L2,L3,L4,TAU)
	END DO
	END DO
	END SUBROUTINE KERNEL
		
	SUBROUTINE INTEGRATOR(TMIN,T,ANS)
	IMPLICIT NONE
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT), INTENT(OUT)::ANS
	REAL(KIND = DBL), INTENT(IN)::TMIN,T
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT)::L1,L2,L3,L4,SUMM
	REAL(KIND = DBL)::TAU
	SUMM(:,:) = (0.0D0,0.0D0)
	TAU = TMIN
	DO WHILE ((T - TAU) > 0.000000001D0)
	CALL KERNEL(TAU,L1)
	CALL KERNEL(TAU + STEPSIZE_TAU,L2)
	CALL KERNEL(TAU + (STEPSIZE_TAU/2.0D0),L3)
	L4 = (L1 + L2) + 4.0D0*L3 
	SUMM = SUMM + (L4 *(STEPSIZE_TAU/6.0D0))
	TAU = TAU + STEPSIZE_TAU
	END DO
	ANS(:,:) = SUMM(:,:)
	END SUBROUTINE INTEGRATOR
		
	SUBROUTINE RUNGEKUTTA(T_MAX,ANSWER)
	IMPLICIT NONE
	REAL(KIND = DBL),INTENT(IN)::T_MAX
	COMPLEX*16, DIMENSION(DIMMAT,1), INTENT(OUT) :: ANSWER
	COMPLEX*16, DIMENSION(DIMMAT,1)::K_1,K_2,K_3,K_4,X
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT)::L1,L2,L3,P1,P2,P3,P4,P5
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT)::DELTAARRAY
	COMPLEX*16, DIMENSION(DIMMAT,DIMMAT)::INTEGRAL	
	REAL(KIND = DBL) :: Z_EXPECTATION, X_EXPECTATION
	REAL(KIND = DBL)::T
	COMPLEX*16, DIMENSION(DIMSYS,DIMSYS)::RHO_EB
	T = 0.00000000001D0
	INTEGRAL(:,:) = (0.0D0, 0.0D0)
	X = INSTATE_BASISSTATES
	CALL COHEVOL(DELTAARRAY)
	L1 = DELTAARRAY
	DO WHILE(T < T_MAX)
	CALL EXPECTATION(X,JZ_EB, Z_EXPECTATION)!making changes here
	WRITE(1,*) T, Z_EXPECTATION/JSYS
	CALL EXPECTATION(X,JX_EB, X_EXPECTATION) !making changes here
	WRITE(3,*) T, X_EXPECTATION/JSYS
	IF (T < TCUTOFF) THEN
	CALL INTEGRATOR(T,(T + STEPSIZE_T/2.0D0),P1)
	CALL INTEGRATOR(T, T + STEPSIZE_T,P2)
	CALL BRIG((T + STEPSIZE_T/2.0D0),P3)
	CALL BRIG(T + STEPSIZE_T,P4)
	L2 = DELTAARRAY + INTEGRAL + P1 + P3
	L3 = DELTAARRAY + INTEGRAL + P2 +P4
	INTEGRAL = INTEGRAL + P2  !making changes here
	END IF
	K_1 = MATMUL(L1,X)
	K_2=MATMUL(L2,(X+0.5D0*STEPSIZE_T*K_1))
	K_3=MATMUL(L2,(X+0.5D0*STEPSIZE_T*K_2))
	K_4 = MATMUL(L3,(X + (STEPSIZE_T*K_3)))
	X = X + ((STEPSIZE_T/6.0D0)*(K_1+2.0D0*K_2+2.0D0*K_3+K_4))
	T = T + STEPSIZE_T
	IF (T < TCUTOFF) THEN
	L1 = L3
	END IF
	END DO
	ANSWER = X
	END SUBROUTINE RUNGEKUTTA

		
		
