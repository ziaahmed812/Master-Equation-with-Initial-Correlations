def test_exact_output_has_headers_and_overwrite_protection(tmp_path: Path) -> None:
    out = tmp_path / "pure"
    run_simulation(
        SimulationParams(
            bath="bosonic",
            model="pure-dephasing",
            spectral="ohmic",
            N=1,
            epsilon=4.0,
            delta0=0.0,
            delta=0.0,
        ),
        out,
        verbose=False,
    )
    assert (out / "exact-correlated.dat").read_text().startswith("# Generated by")
    with pytest.raises(FileExistsError):
        run_simulation(
            SimulationParams(
                bath="bosonic",
                model="pure-dephasing",
                spectral="ohmic",
                N=1,
                epsilon=4.0,
                delta0=0.0,
                delta=0.0,
            ),
            out,
            verbose=False,
        )


def test_solver_class_api_defaults_to_current_directory(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.chdir(tmp_path)
    solver = PureDephasingSolver(system=SystemParams(N=1, epsilon=4.0))
    result = solver.run(RunConfig(verbose=False))
    assert result.output_dir == tmp_path
    assert (tmp_path / "exact-correlated.dat").exists()


