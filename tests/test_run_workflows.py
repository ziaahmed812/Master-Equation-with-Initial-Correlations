@pytest.mark.solver_rerun
def test_run_bosonic_spin_boson_n2(tmp_path: Path) -> None:
    if not _compiler_ready():
        pytest.skip("gfortran not available")
    result = run_simulation(
        SimulationParams(bath="bosonic", model="spin-boson", spectral="ohmic", observable="jx", N=2),
        tmp_path / "spin-boson-n2",
        verify=True,
    )
    assert result.correlated_error is not None
    assert result.correlated_error <= 1.0e-6
    assert result.uncorrelated_error is not None
    assert result.uncorrelated_error <= 1.0e-6
    assert (result.output_dir / "observable-correlated.dat").read_text().startswith("# Generated by")
    assert "input_mode: generated" in (result.output_dir / "observable-correlated.dat").read_text()
    assert (result.output_dir / "EXPX-C.DAT").exists()
    assert (result.output_dir / "sources" / "correlated_solver.f").exists()
    assert (result.output_dir / "sources" / "dimensions.inc").exists()
    assert (result.output_dir / "INSTATE.dat").exists()
    assert result.log_files is not None
    assert "correlated_compile_stderr" in result.log_files
    with pytest.raises(FileExistsError):
        run_simulation(
            SimulationParams(bath="bosonic", model="spin-boson", spectral="ohmic", observable="jx", N=2),
            result.output_dir,
            verify=False,
            verbose=False,
        )


