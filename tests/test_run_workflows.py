@pytest.mark.solver_rerun
def test_bosonic_custom_matrix_observable_and_initial_state(tmp_path: Path) -> None:
    if not _compiler_ready():
        pytest.skip("gfortran not available")
    custom_observable = np.eye(3, dtype=complex) / 2.0
    custom_initial = np.eye(3, dtype=complex) / 3.0
    result = run_simulation(
        SimulationParams(
            bath="bosonic",
            model="spin-boson",
            spectral="superohmic",
            observable=custom_observable,
            N=2,
            s=2.0,
            initial_state=custom_initial,
        ),
        tmp_path / "bosonic-custom-matrix-state",
        verify=False,
        verbose=False,
        numerics=FAST_NUMERICS,
    )
    assert result.example is None
    assert "observable: custom_matrix" in (result.output_dir / "observable-correlated.dat").read_text()
    assert "initial_state_source: user_supplied" in (result.output_dir / "INSTATE.dat").read_text()
